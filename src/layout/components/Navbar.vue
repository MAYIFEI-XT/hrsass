      this.$store.dispatch('app/toggleSideBar')
    },
    logout() {
      // 这里写不写await都可以，登出方法是同步的
      this.$store.dispatch('user/logout')
      this.$router.push(`/login`) // 直接跳到登陆页
    }
  }
}
